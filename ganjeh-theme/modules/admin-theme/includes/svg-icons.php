    /**
     * جایگزینی آیکون‌های منو
     */
    public function replace_menu_icons() {
        global $menu;
        
        $icon_map = [
            'index.php' => 'dashboard',
            'edit.php' => 'posts',
            'upload.php' => 'media',
            'edit.php?post_type=page' => 'pages',
            'edit-comments.php' => 'comments',
            'themes.php' => 'appearance',
            'plugins.php' => 'plugins',
            'users.php' => 'users',
            'tools.php' => 'tools',
            'options-general.php' => 'settings',
        ];
        
        foreach ($menu as $key => $item) {
            if (isset($item[2]) && isset($icon_map[$item[2]])) {
                $icon_name = $icon_map[$item[2]];
                if (isset($this->icons[$icon_name])) {
                    $menu[$key][6] = 'data:image/svg+xml;base64,' . base64_encode($this->icons[$icon_name]);
                }
            }
        }
    }
    
